/*function to configure the LCD*/
LCD_config Configure_LCD(void)
{
	LCD_config LCD;
	LCD.type = TWO_LINE_LCD_Eight_BIT_MODE;
	LCD.cursor = CURSOR_ON;
	return LCD;
}


